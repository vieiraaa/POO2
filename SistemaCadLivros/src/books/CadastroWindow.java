    private void btnAntesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAntesActionPerformed
        if (regAtual > 1){
            regAtual--;
            carregaDados(regAtual);
            edit = true;
        }
    }//GEN-LAST:event_btnAntesActionPerformed

    private void btnDpsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDpsActionPerformed
       if (regAtual < dados.getnRegistros()-1){
           regAtual ++;
            carregaDados(regAtual);
            edit = true;
       }
    }//GEN-LAST:event_btnDpsActionPerformed

    
