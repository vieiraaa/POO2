    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        String nome = txtItem.getText();
        String desc = txtDesc.getText();   
        String aut = txtaut.getText();
        
        NovoItem novo = new NovoItem(nome, desc, aut);
        
        if (edit) {
            dados.edit(regAtual, novo);
            
        } else {
            
        regAtual = dados.getnRegistros();
        dados.add(novo);
        }
    JOptionPane.showMessageDialog(this, "Seu item foi cadastrado com sucesso!\n");
    }//GEN-LAST:event_btnSalvarActionPerformed

